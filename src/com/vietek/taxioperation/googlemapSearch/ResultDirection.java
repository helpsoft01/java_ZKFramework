package com.vietek.taxioperation.googlemapSearch;

public class ResultDirection {

	ValueDirection distance;
	ValueDirection duration;
	String status;

	public ValueDirection getDistance() {
		return distance;
	}

	public void setDistance(ValueDirection distance) {
		this.distance = distance;
	}

	public ValueDirection getDuration() {
		return duration;
	}

	public void setDuration(ValueDirection duration) {
		this.duration = duration;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
